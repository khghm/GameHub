@Serializable
data class HexCell(
    val row: Int,
    val col: Int,
    val state: CellState = CellState.Closed,
    val letter: Char? = null,
    val owner: PlayerId? = null
)

@Serializable
enum class CellState {
    Closed,
    OpenNeutral,
    Captured
}

@Serializable
data class BaltazarState(
    val players: List<PlayerId>,
    val board: List<HexCell>,
    val currentPlayer: PlayerId?,
    val selectedCells: List<Pair<Int, Int>> = emptyList(),
    val timeoutsA: Int = 0,
    val timeoutsB: Int = 0,
    val winner: PlayerId? = null
) : GameState() {

    companion object {
        fun initial(players: List<PlayerId>): BaltazarState {
            val fullPlayers = if (players.size < 2) {
                players + PlayerId("BOT_1")
            } else players

            val player1 = fullPlayers[0]
            val player2 = fullPlayers[1]

            val initialBoard = mutableListOf<HexCell>()
            val validCells = getValidCells()
            for ((r, c) in validCells) {
                var cell = HexCell(r, c, CellState.Closed, null, null)

                if (r == 0 && c == 1) {
                    // خانه اصلی بازیکن اول
                    cell = HexCell(r, c, CellState.Captured, null, player1)
                } else if (r == 6 && c == 5) {
                    // خانه اصلی بازیکن دوم
                    cell = HexCell(r, c, CellState.Captured, null, player2)
                }

                initialBoard.add(cell)
            }

            for ((r, c) in getHomeNeighbors(0, 1)) {
                openCell(initialBoard, r, c)
            }
            for ((r, c) in getHomeNeighbors(6, 5)) {
                openCell(initialBoard, r, c)
            }

            val firstPlayer = fullPlayers.random()

            return BaltazarState(
                players = fullPlayers,
                board = initialBoard,
                currentPlayer = firstPlayer
            )
        }

        private fun getValidCells(): List<Pair<Int, Int>> {
            return listOf(
                (0 to 0), (0 to 1), (0 to 2), (0 to 3), (0 to 4),
                (1 to 0), (1 to 1), (1 to 2), (1 to 3), (1 to 4),
                (2 to 0), (2 to 1), (2 to 2), (2 to 3), (2 to 4), (2 to 5),
                (3 to 0), (3 to 1), (3 to 2), (3 to 3), (3 to 4), (3 to 5), (3 to 6),
                (4 to 1), (4 to 2), (4 to 3), (4 to 4), (4 to 5), (4 to 6),
                (5 to 2), (5 to 3), (5 to 4), (5 to 5), (5 to 6),
                (6 to 2), (6 to 3), (6 to 4), (6 to 5), (6 to 6)
            )
        }

        private fun getHomeNeighbors(r: Int, c: Int): List<Pair<Int, Int>> {
            return if (r % 2 == 0) {
                listOf(r-1 to c-1, r-1 to c, r to c-1, r to c+1, r+1 to c-1, r+1 to c)
            } else {
                listOf(r-1 to c, r-1 to c+1, r to c-1, r to c+1, r+1 to c, r+1 to c+1)
            }.filter { getValidCells().contains(it) }
        }

        private fun openCell(board: MutableList<HexCell>, r: Int, c: Int) {
            val idx = board.indexOfFirst { it.row == r && it.col == c }
            if (idx != -1) {
                val letter = getRandomPersianLetter()
                board[idx] = board[idx].copy(
                    state = CellState.OpenNeutral,
                    letter = letter,
                    owner = null
                )
            }
        }
    }

    fun getCell(row: Int, col: Int): HexCell? {
        return board.find { it.row == row && it.col == col }
    }

    fun getTerritory(playerId: PlayerId): List<Pair<Int, Int>> {
        val home = if (playerId == players[0]) (0 to 1) else (6 to 5)
        val territory = mutableListOf(home)
        val visited = mutableSetOf<Pair<Int, Int>>()
        visited.add(home)

        val queue = mutableListOf(home)
        while (queue.isNotEmpty()) {
            val (r, c) = queue.removeAt(0)
            val neighbors = getValidNeighbors(r, c)
            for ((nr, nc) in neighbors) {
                val cell = getCell(nr, nc)
                if (cell != null && cell.owner == playerId && !visited.contains(nr to nc)) {
                    visited.add(nr to nc)
                    territory.add(nr to nc)
                    queue.add(nr to nc)
                }
            }
        }
        return territory
    }

    fun getValidNeighbors(r: Int, c: Int): List<Pair<Int, Int>> {
        val dirs = if (r % 2 == 0) {
            listOf(-1 to -1, -1 to 0, 0 to -1, 0 to 1, 1 to -1, 1 to 0)
        } else {
            listOf(-1 to 0, -1 to 1, 0 to -1, 0 to 1, 1 to 0, 1 to 1)
        }
        val validCells = listOf(
            (0 to 0), (0 to 1), (0 to 2), (0 to 3), (0 to 4),
            (1 to 0), (1 to 1), (1 to 2), (1 to 3), (1 to 4),
            (2 to 0), (2 to 1), (2 to 2), (2 to 3), (2 to 4), (2 to 5),
            (3 to 0), (3 to 1), (3 to 2), (3 to 3), (3 to 4), (3 to 5), (3 to 6),
            (4 to 1), (4 to 2), (4 to 3), (4 to 4), (4 to 5), (4 to 6),
            (5 to 2), (5 to 3), (5 to 4), (5 to 5), (5 to 6),
            (6 to 2), (6 to 3), (6 to 4), (6 to 5), (6 to 6)
        )
        return dirs.map { (dr, dc) -> r + dr to c + dc }
            .filter { validCells.contains(it) }
    }
}
