// Frequency-based Persian letters (common letters have higher weights)
private val PERSIAN_LETTERS_WITH_WEIGHTS = listOf(
    'ا' to 15, 'ی' to 14, 'ر' to 12, 'ن' to 11, 'ه' to 10,
    'و' to 9, 'م' to 8, 'ت' to 7, 'ب' to 7, 'س' to 7,
    'ل' to 6, 'ک' to 6, 'ش' to 6, 'د' to 5, 'خ' to 5,
    'ع' to 5, 'غ' to 4, 'ف' to 4, 'ق' to 4, 'ح' to 4,
    'ج' to 4, 'چ' to 3, 'پ' to 3, 'ژ' to 2, 'ث' to 2,
    'ص' to 2, 'ض' to 2, 'ط' to 2, 'ظ' to 2, 'ذ' to 2,
    'ز' to 5, 'ء' to 1
)

// Create a weighted list for random selection
private val WEIGHTED_LETTERS = buildList {
    PERSIAN_LETTERS_WITH_WEIGHTS.forEach { (letter, weight) ->
        repeat(weight) { add(letter) }
    }
}

// Function to get a random letter based on frequency
fun getRandomPersianLetter(): Char {
    return WEIGHTED_LETTERS.random()
}

// Unique letters from BALTAR_VOCAB for the board (fallback)
